         <h2>Letterform Composition Video Transcript</h2>
         <p>Letterform Compositions: Using Helvetica and Adobe Garamond.</p>
         <p>There is a famous line from Shakespeare’s Romeo and Juliet which says,
             “That which we call a rose by any other name would smell as sweet” 
             (<em>Romeo and Juliet</em> II, ii, 1-2). In this phrase Juliet was 
             claiming that names are meaningless; that it wouldn’t really matter
             what we call a rose it would still smell sweet no matter what.</p>
         <p>However, since I have started studying typography I have learned that
             names really do have a strong meaning. They can tell us the difference
             between letterform characteristics.</p>
         <p>The first letterform composition I created was using Helvetica. Now 
             Helvetica has even strokes, and it uses pretty straight lines throughout
             the composition. The letterforms are pretty strong letterforms. They
             all wanted to stand out when I was creating this composition. 
             Helvetica is a sans serif font meaning they don’t have caps or tags 
             at the end of their letters. Because of this it makes it a very easy 
             font to read at small sizes as well as large sizes. Helvetica has 
             even line strokes so it makes nice shapes, like circles. The O and
             E make circles.  Because of that I can use these shapes to make 
             focal points and good alignment to direct the eye within the piece.</p>
         <p>Adobe Garamond has a very different feel to it. It has serifs or caps
             and varying stroke width. At the top of the s the line is thinner
             and then is thickens through the spine of the s. Because of this, it
             has a very different feel than Helvetica does. Right here you can see
             it is a circle but because of the varying line stroke of the g it makes
             it more oval. The varying stoke width made it more difficult to create
             alignment></p>
         <p>So you can see between the two fonts, there really is a different. 
             When you hear the word Helvetica it’s going to be a very different 
             feel and those fonts will be used for a different use than Adobe 
             Garamond. They both serve their purpose but their names provide very
             different meanings based on the qualities and characteristics of the
             fonts.</p>
