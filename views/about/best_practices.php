                    <h2>Best Practices I Follow</h2>
                    <h3>Page Layout</h3>
                    <ul>
                        <li>Consistent site header/logo</li>
                        <li>Consistent Navigation Area</li>
                        <li>Informative Page Title</li>
                        <li>Page footer area includes last update, contact email</li>
                        <li>Balance of text/graphics/white space on page.</li>
                        <li>Readable contrast between text and background</li>
                        <li>Home Page has compelling, interesting information above the fold
                            at 1024x868 resolution</li>
                        <li>Viewport meta tag is used to enhance display on smartphones</li>
                        <li>Media queries configure responsive page layout for smartphone
                            and table display</li>
                    </ul>
                    <h3>Browser Compatibility</h3>
                    <ul>
                        <li>Displays on popular/current versions
                            <ul>
                                <li>Internet Explorer</li>
                                <li>Firefox</li>
                                <li>Google Chrome</li>
                                <li>Opera</li>
                                <li>Safari</li>
                            </ul> 
                        </li>
                        <li>Displays on popular mobile devices</li>
                    </ul>
                    <h3>Navigation</h3>
                    <ul>
                        <li>Main navigation links are clearly and consistently labeled</li>
                        <li>Navigation is easy to use for target audience</li>
                        <li>If main navigation uses images, clear text links are in the footer
                            section of the page</li>
                        <li>All navigation hyper-links work</li>
                    </ul>
                    <h3>Color and Graphics</h3>
                    <ul>
                        <li>Color scheme is limited to a maximum of four colors</li>
                        <li>Color is used consistently</li>
                        <li>Text color has sufficient contrast with background color</li>
                        <li>Color alone is not used to convey meaning</li>
                        <li>Color and graphics are used only to enhance the site</li>
                        <li>Graphics do not significantly slow download</li>
                    </ul>
                    <h3>Content Presentation</h3>
                    <ul>
                        <li>Common fonts are used</li>
                        <li>Web writing techniques utilized: headings, bullet points,
                            brief paragraphs</li>
                        <li>Fonts, font sizes, and colors are consistent</li>
                        <li>Content provides meaningful, useful information</li>
                        <li>Content is organized consistently</li>
                        <li>Information is easy to find</li>
                        <li>Content does not include outdate material</li>
                        <li>Content is free of typographical and grammatical errors</li>
                        <li>Content provides links to other useful sites</li>
                        <li>Avoids the use of "Click here" when writing text for
                            hyper-links</li>
                        <li>Hyper-links are formatted consistently</li>
                        <li>If graphics or media are used to convey meaning, the alternate
                            text equivalent is provided.</li>
